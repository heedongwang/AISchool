package for문;

public class EX00for문 {

	public static void main(String[] args) {
		/*반복문: 특정 명령을 반복할 수 있도록 도와주는 문법(while, do-while, for )
		 1.for:반복횟수 정해져 있을떄
		 	for(초기화;조건;반복작업)=>(시작;도착;보폭)
		 */
		
		for(int i=1; i<6;i++) {
			System.out.print(i+" ");
		}
		
		
		System.out.println("\n");
		
		
		for(int i=1;i<=10;i++) {
			System.out.print(i+" ");
		}
	}

}
